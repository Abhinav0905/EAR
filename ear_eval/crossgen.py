"""Cross-family GENERATION experiment: run one corpus end-to-end with a non-Claude
generator (OpenAI GPT-4o-mini) while the Claude Haiku judge scores, closing the
model-family threat on the generation side (the judge side is closed by judge_openai).

The pipeline generator (rewrite/cover/critic/draft/final answer calls) becomes GPT-4o-mini;
retrieval, the EAR SLM, the cross-encoder, question sets, and the judge are unchanged.

  python -m ear_eval.crossgen --corpus wmp --n 100
  python -m ear_eval.crossgen --analyze
"""
import argparse
import json
from collections import defaultdict

import numpy as np

from . import config, stats
from .run import run_corpus, CORE_VARIANTS

GEN = "gpt-4o-mini"
TAG = "wmp_gpt4ominiGen"


def run(corpus="wmp", n=100, judge_runs=3, max_workers=6):
    run_corpus(corpus, n, CORE_VARIANTS, judge_runs=judge_runs, gen_model=GEN, tag=TAG, max_workers=max_workers)
    analyze(corpus)


def _load(tag):
    f = config.RESULTS_DIR / f"{tag}_checkpoint.jsonl"
    d = {}
    for l in f.read_text().splitlines():
        r = json.loads(l)
        d[(r["variant"], r["qid"])] = r
    return d


def analyze(corpus="wmp"):
    gpt = _load(TAG)              # cross-family generation (gpt-4o-mini), Haiku judge
    son = _load(corpus)          # Claude generation (Sonnet), Haiku judge
    byv = defaultdict(list)
    for (v, q), r in gpt.items():
        byv[v].append(r)

    print(f"\n{'='*70}\nCROSS-FAMILY GENERATION: {corpus} generated by {GEN}, judged by Haiku\n{'='*70}")
    print(f"{'variant':16s} {'Gen':>5s} {'Ret':>5s} {'calls':>6s} {'$/query':>9s}")
    for v in CORE_VARIANTS:
        rs = byv.get(v, [])
        if not rs:
            continue
        g = np.mean([r["generation_avg"] for r in rs]); rt = np.mean([r["retrieval_avg"] for r in rs])
        lc = np.mean([r["metrics"]["llm_calls"] for r in rs]); cost = np.mean([r["metrics"]["gen_cost_usd"] for r in rs])
        print(f"{v:16s} {g:>5.2f} {rt:>5.2f} {lc:>6.1f} {cost:>9.5f}")

    # EAR vs each baseline under gpt-4o-mini generation (paired Wilcoxon, generation axis)
    print(f"\nEAR vs baseline under {GEN} generation (gen Δ, paired Wilcoxon):")
    ear = {q: r for (v, q), r in gpt.items() if v == "ear_full"}
    out = {"corpus": corpus, "generator": GEN, "judge": "haiku", "variant_means": {}, "ear_vs_baseline": {}}
    for v in CORE_VARIANTS:
        rs = byv.get(v, [])
        if rs:
            out["variant_means"][v] = {"gen": float(np.mean([r["generation_avg"] for r in rs])),
                                       "ret": float(np.mean([r["retrieval_avg"] for r in rs])),
                                       "calls": float(np.mean([r["metrics"]["llm_calls"] for r in rs])),
                                       "cost": float(np.mean([r["metrics"]["gen_cost_usd"] for r in rs]))}
    for b in [x for x in CORE_VARIANTS if x != "ear_full"]:
        bq = {q: r for (v, q), r in gpt.items() if v == b}
        common = sorted(set(ear) & set(bq))
        if not common:
            continue
        w = stats.paired_wilcoxon([ear[q]["generation_avg"] for q in common], [bq[q]["generation_avg"] for q in common])
        d = stats.cohens_d_paired([ear[q]["generation_avg"] for q in common], [bq[q]["generation_avg"] for q in common])
        sig = "✗" if w["p"] < 0.05 else "~"
        print(f"  vs {b:16s} Δ={w['mean_diff']:+.3f} p={w['p']:.3f}{sig} d={d:+.3f}")
        out["ear_vs_baseline"][b] = {"delta": w["mean_diff"], "p": w["p"], "d": d, "n": len(common)}

    # Does swapping the generator (Sonnet -> gpt-4o-mini) change EAR's quality? paired per-question
    print(f"\nGenerator swap effect on EAR (Sonnet -> {GEN}, same questions, Haiku judge):")
    swap = {}
    for v in CORE_VARIANTS:
        common = sorted({q for (vv, q) in gpt if vv == v} & {q for (vv, q) in son if vv == v})
        if not common:
            continue
        a = [gpt[(v, q)]["generation_avg"] for q in common]
        b = [son[(v, q)]["generation_avg"] for q in common]
        w = stats.paired_wilcoxon(a, b)
        swap[v] = {"gpt_gen": float(np.mean(a)), "sonnet_gen": float(np.mean(b)),
                   "delta_gpt_minus_sonnet": w["mean_diff"], "p": w["p"], "n": len(common)}
        print(f"  {v:16s} gpt {np.mean(a):.2f} vs sonnet {np.mean(b):.2f}  Δ={w['mean_diff']:+.3f} p={w['p']:.3f}")
    out["generator_swap_effect"] = swap

    outpath = config.RESULTS_DIR / "crossgen_gpt4omini.json"
    outpath.write_text(json.dumps(out, indent=2))
    print(f"\n[out] {outpath}")
    return out


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--corpus", default="wmp")
    ap.add_argument("--n", type=int, default=100)
    ap.add_argument("--judge-runs", type=int, default=3)
    ap.add_argument("--max-workers", type=int, default=6)
    ap.add_argument("--analyze", action="store_true")
    args = ap.parse_args()
    if args.analyze:
        analyze(args.corpus)
    else:
        run(args.corpus, args.n, judge_runs=args.judge_runs, max_workers=args.max_workers)


if __name__ == "__main__":
    main()
